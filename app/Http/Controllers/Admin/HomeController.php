    public function update(Request $request, $user)
    {
        $user = User::find($user);
        $newPassword = $request->get('contraseñaUser');
        $clave = Hash::make($newPassword);
        $user ->password = $clave;
        $user->changePassword=1;

        $user ->save();
        return redirect()->route('directiva.index', $user)->with('status','se ha cambiado la contraseña');
    }

